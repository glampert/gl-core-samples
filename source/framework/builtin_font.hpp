// Built-in font glyph bitmap decompression:
// ========================================================

// If you decide to change the font, these are the only things that
// need to be updated. The global variables are never referenced
// directly in the code, these functions are used instead.
static const std::uint8_t * getRawFontBitmapData() { return fontShareTechMono20Bitmap;  }
static const FontCharSet  & getFontCharSet()       { return fontShareTechMono20CharSet; }

static int rleDecode(const std::uint8_t * input, const int inSizeBytes,
                     std::uint8_t * output, const int outSizeBytes)
{
    int bytesWritten = 0;
    std::uint8_t rleCount = 0;
    std::uint8_t rleByte  = 0;

    for (int i = 0; i < inSizeBytes; i += 2)
    {
        rleCount = *input++;
        rleByte  = *input++;

        // Replicate the RLE packet.
        while (rleCount--)
        {
            *output++ = rleByte;
            if (++bytesWritten == outSizeBytes && rleCount != 0)
            {
                // Reached end of output and we are not done yet, stop with an error.
                return -1;
            }
        }
    }

    return bytesWritten;
}

static std::uint8_t * decompressFontBitmap()
{
    const int compressedSize = fontShareTechMono20BitmapSizeBytes;
    const std::uint8_t * compressedBuffer = getRawFontBitmapData();

    const int uncompressedSize = getFontCharSet().bitmapDecompressSize;
    auto * uncompressedBuffer  = new std::uint8_t[uncompressedSize];

    const int result = rleDecode(compressedBuffer,   compressedSize,
                                 uncompressedBuffer, uncompressedSize);

    if (result != uncompressedSize)
    {
        delete[] uncompressedBuffer;
        return nullptr; // Error
    }

    return uncompressedBuffer; // Caller responsible for freeing it
}

